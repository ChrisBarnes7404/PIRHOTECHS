			<h4>About the FIRST Robotics Competition</h4>
				<p><span style="font-family: helvetica; font-size: medium;">The 
				<a title="What is FIRST" href="file:///C:/Users/cbarn/Desktop/Website%20Progress/Robotics--4/about/about/WhatFIRST.php">FIRST</a> Robotics Competition challenges teams of students and their mentors to solve a common problem in a six-week timeframe using a standard "kit of parts" and a common set of rules. Teams build robots from the parts and enter them in a series of competitions designed by Dean Kamen and Woodie Flowers. The Competition has grown to over 900 teams competing in 26 Regional Events, and The Championship held at the Georgia Dome in Atlanta where more than 7,000 students participate.
				<br /><br />
				FIRST redefines winning for these students. Teams are rewarded for excellence in design, demonstrated team spirit, gracious professionalism and maturity, and ability to overcome obstacles. Scoring the most points is a secondary goal. Winning means building partnerships that last.
				<br /><br />
				<a title="FIRST Website" href="http://www.usfirst.org/" target="_blank">FIRST</a>
				<br />
				<a title="FIRST Robotics Competition" href="http://www.usfirst.org/roboticsprograms/frc" target="_blank">FIRST Robotics Competition</a>
				</span></p>
			
			
			<h4>About Team Structure</h4>
				<p><span style="font-family: helvetica; font-size: medium;">For specialized work, the team is divided into the following subteams:</span></p>
				<ul>
					<li><span style="font-family: helvetica; font-size: medium;"><span style="color: #b50000;"><strong>Leadership:</strong></span> This subteam is responsible for creating the team structure, determining the team calendar, setting up team activities and trainings, and managing school relations.</span></li>
					
					<li><span style="font-family: helvetica; font-size: medium;"><span style="color: #b50000;"><strong>Mechanical:</strong></span> This subteam is responsible for designing the mechanical components of the robot, as well as putting them together. Components are designed in CAD, and sometimes a prototype is created before final assembly.</span></li>
					
					<li><span style="font-family: helvetica; font-size: medium;"><span style="color: #b50000;"><strong>Electrical:</strong></span> This subteam is responsible for designing and producing all electrical systems for the robot and control panel. It is also responsible for any pneumatics that are used.</span></li>
					
					<li><span style="font-family: helvetica; font-size: medium;"><span style="color: #b50000;"><strong>Programming:</strong></span> This subteam is responsible for creating the actual code that makes the robot work. It also creates the autonomous code(s) for the competition.</span></li>
					
					<li><span style="font-family: helvetica; font-size: medium;"><span style="color: #b50000;"><strong>Webpage:</strong></span> This subteam is responsible for creating and maintaining the webpage, forums, blog, wiki, and smugmug photo database. They also work to unify our teams social media and web presence.</span></li>

					<li><span style="font-family: helvetica; font-size: medium;"><span style="color: #b50000;"><strong>Strategy:</strong></span> This subteam is responsible for actually controlling and driving the robot. They are also the rules experts of the team, and have created strategies for the best scores.</span></li>

					<li><span style="font-family: helvetica; font-size: medium;"><span style="color: #b50000;"><strong>Marketing:</strong></span> This subteam creates all presentations, press releases, newsletters and any other public relations propaganda. They are also responsible for team logos, clothing, and team spirit.</span></li>

					<li><span style="font-family: helvetica; font-size: medium;"><strong><span style="color: #b50000;">Animation:</span>&nbsp;</strong>This subteam learns the ins and outs of 3D Studio Max in preparation for the Animation competition each year.</span></li>
				</ul>
