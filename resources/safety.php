			<h1><span style="font-size: medium;">Team 2927 Safety Handbook</span></h1>
				<div style="padding-left: 30px;">
					<p style="font-size: medium; text-decoration: underline"><strong>Mission Statement</strong></p>
					<p>
						We pledge to try to keep everyone safe at all times.
						Safety goes further than just the pit area, we will carry it into our own shop area and our every day lives.
						Safety can be as simple as announcing to everyone when you come through transporting large objects, and as serious as wearing safety glasses and gloves when grinding metal.
						Safety doesn't have to be boring. If safety is fun, more people will use it not as a practice, but as a good habit.
						In order for others to be safe, you must set constant examples of safe behavior and educate others on how to be safe.
						Don't just tell someone that they are doing something unsafe, explain to them the dangers of their actions and how they can use safety in that situation.
					</p>
					<p><a title="Safety Handbook" href="resources/Safety/Safety_Handbook.pdf" target="_blank">Team safety handbook</a></p>
