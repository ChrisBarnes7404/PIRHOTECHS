			<h1>Scholarships</h1>
				<p>Many corporations, colleges and universities offer scholarships to high school students on <em>FIRST</em> teams. These scholarships show official recognition of the technical knowledge and life skills students have gained from participating in <em>FIRST</em> competitions.</p>
				<p><em><strong>There are many scholarship opportunities out there for STEM and non-STEM majors.</strong></em> This page is a resource for scholarships currently available and any other new scholarships our team learns of, so check back often!</p>
				<p><em>FIRST </em>alone has almost <strong>900 individual scholarship opportunities</strong> with a total value of <strong>more than $19,000,000.</strong> </p>
				<h4>Note the due dates</h4>
					<p>Because many due date are short spanned only lasting a 
					few months, they tend to have already past, if this is the case please <a href="contact.php" target="_blank">Contact Us</a> 
					and let us know, so that we may update this page.</p>
			<hr />
			<h4><span style="font-size: medium;"><em>FIRST</em> Scholarships</span></h4>
				<p><span style="font-size: medium; font-family: helvetica;">All <strong></strong><em>FIRST</em> scholarships can be found at this <a href="http://www.usfirst.org/aboutus/scholarships" target="_blank">site</a>.</span></p>
				<p>There is also a <a href="https://www.firstinspires.org/community/newsletter"><em>FIRST </em>Scholarship Newsletter</a> that you can subscribe to for special information.</p>
