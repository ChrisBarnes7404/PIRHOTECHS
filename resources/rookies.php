			<h1>Rookie Organization</h1>
				<p>To all rookies!<br />
				Here, we have provided some documentation that we are using and have found helpful over the years. We hope this helps any rookie teams out there!&nbsp; Items here are organized in a Timeline fashion so that teams can work through step by step organization.</p>
			<h4><span style="color: #b50000;">Summer</span> - <span style="color: #cccccc;">June-August</span></h4>
				<p>1. Plan out your Budget.&nbsp; Decide how much money you will need to run your team.</p>
				<ul>
					<li>See our 
					<a title="Budget Estimate" href="resources/Rookies/Budget Estimate.doc" target="_blank">budget estimate</a> from the 2005 and 2006 Seasons (current estimate coming soon!)</li>
					<li>FRC Teams run on anywhere from $8,000 to $100,000+</li>
				</ul>
				<p>2. Plan out your Pre-Season.&nbsp; Decide what the team will do and how the team will be organized.</p>
				<ul>
					<li>We planned out the tasks we wanted to do in Pre-Season in this 
					<a title="Pre-Season Plan" href="resources/Rookies/Summer and Preseason.doc" target="_blank">document.</a> It includes potential team projects, 
					sub team descriptions, engineering competitions for team meetings, mentor presentations, and some general tasks.</li>
